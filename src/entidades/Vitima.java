public class Vitima extends Personagem{
    
    private int id;

    public Vitima(int id, String nome, String sobrenome, Contacto contacto) {
        super(nome, sobrenome, contacto);
        this.id = id;
    }

    public Vitima(int id) {
        this.id = id;
    }

    public Vitima() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getSobrenome() {
        return sobrenome;
    }

    public void setSobrenome(String sobrenome) {
        this.sobrenome = sobrenome;
    }

    public Contacto getContacto() {
        return contacto;
    }

    public void setContacto(Contacto contacto) {
        this.contacto = contacto;
    }
    
}
